package main

import "fmt"

func main() {
	//bool - true/false
	fmt.Printf("Type: %T - Value: %v\n", true, true)

	//string - sequênciq de bytes
	fmt.Printf("Type: %T - Value: %v\n", "yuri", "yuri")

	//int - números
	fmt.Printf("Type: %T - Value: %v\n", 1, 1)

	//float
	fmt.Printf("Type: %T - Value: %v\n", 1.233, 1.233)
}
